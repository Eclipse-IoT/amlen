package com.ibm.ims.svt.clients;

import java.io.Serializable;
import java.util.UUID;


/**
 * This class can be used to pass objects around in JMS Object messages
 *
 */
public class PersonObject implements Serializable {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 8186402277235951108L;
	
	private String firstName = UUID.randomUUID().toString().substring(0, 5);
	private String lastName = UUID.randomUUID().toString().substring(0, 5);
	private int age = 42;
	private String location = UUID.randomUUID().toString().substring(0, 5);
	
	public PersonObject()
	{
		
	}
	
	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public String toString() {
		return ("Person Object: First Name: " + this.firstName + "; Last Name: "+  this.lastName + "; Location: " + this.location + "; Age: " + this.age);
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + age;
		result = prime * result
				+ ((firstName == null) ? 0 : firstName.hashCode());
		result = prime * result
				+ ((lastName == null) ? 0 : lastName.hashCode());
		result = prime * result
				+ ((location == null) ? 0 : location.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		PersonObject other = (PersonObject) obj;
		if (age != other.age)
			return false;
		if (firstName == null) {
			if (other.firstName != null)
				return false;
		} else if (!firstName.equals(other.firstName))
			return false;
		if (lastName == null) {
			if (other.lastName != null)
				return false;
		} else if (!lastName.equals(other.lastName))
			return false;
		if (location == null) {
			if (other.location != null)
				return false;
		} else if (!location.equals(other.location))
			return false;
		return true;
	}

	

}
