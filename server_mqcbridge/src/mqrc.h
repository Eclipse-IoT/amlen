#define mqcCopyBlankPadToNullPad(p1,p2,n)                             \
{                                                                     \
  UINT32 i;                                                           \
  for ( i = 0 ; i < (n) ; i++ )                                       \
  {                                                                   \
    if ( (p2)[i] == ' ' || (p2)[i] == '\0' )                          \
      break;                                                          \
   (p1)[i] = (p2)[i];                                                 \
  }                                                                   \
  (p1)[i] = '\0';                                                     \
}

